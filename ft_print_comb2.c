#include <unistd.h>

void	get_nr(char a)
{
	write(1, &a, 1);
}

void	ft_print_comb2(void)
{
	int	nr1;
	int	nr2;

	nr1 = 0;
	nr2 = 0;
	while (nr1 <= 98)
	{
		nr2 = nr1 + 1;
		while (nr2 <= 99)
		{
			get_nr(nr1 / 10 + '0');
			get_nr(nr1 % 10 + '0');
			get_nr(' ');
			get_nr(nr2 / 10 + '0');
			get_nr(nr2 % 10 + '0');
			if (nr1 != 98 || nr2 != 99)
			{
				write(1, ", ", 2);
			}
			nr2++;
		}
		nr1++;
	}
}
